# Operatori matematici

# somma
x = 10
y = 5

risultato = x + y
print(risultato)

# moltiplicazione
x = 10
y = 5

risultato = x * y
print(risultato)


# modulo
x = 10
y = 5

risultato = x % y
print(risultato)

x = 10
y = 3

risultato = x % y
print(risultato)


# Operatori booleani

x = True
y = False

print(x and y)
print(x or y)
print(not x)
print(x and y and x and x)